Decision tree learning uses a decision tree as a predictive model which maps observations about an item 
to conclusions about the item's target value. It is one of the predictive modelling approaches used in 
statistics, data mining and machine learning. Tree models where the target variable can take a finite set of 
values are called classification trees. In these tree structures, leaves represent class labels and branches 
represent conjunctions of features that lead to those class labels. Decision trees where the target variable 
can take continuous values (typically real numbers) are called regression trees.
In decision analysis, a decision tree can be used to visually and explicitly represent decisions and decision making.
In data mining, a decision tree describes data but not decisions; rather the resulting classification tree can be 
an input for decision making.
